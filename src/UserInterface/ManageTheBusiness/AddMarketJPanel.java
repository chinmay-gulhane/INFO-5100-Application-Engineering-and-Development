    private void btnSelectChannelActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnSelectChannelActionPerformed
        // TODO add your handling code here:
        int selectedRow = tblChannels.getSelectedRow();
         if(selectedRow < 0){
            JOptionPane.showMessageDialog(this, "Please select a channel from the list!","Warning",JOptionPane.WARNING_MESSAGE);
         }
        else{
            tblChannels.setValueAt("Yes", selectedRow, 1);
         }
    }//GEN-LAST:event_btnSelectChannelActionPerformed

    private void btnRemoveChannelActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnRemoveChannelActionPerformed
        // TODO add your handling code here:
        int selectedRow = tblChannels.getSelectedRow();
         if(selectedRow < 0){
            JOptionPane.showMessageDialog(this, "Please select a channel from the list!","Warning",JOptionPane.WARNING_MESSAGE);
         }
        else{
            tblChannels.setValueAt("No", selectedRow, 1);
         }
    }//GEN-LAST:event_btnRemoveChannelActionPerformed

    private void btnSaveActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnSaveActionPerformed
        // TODO add your handling code here:
        Market m = new Market(txtName.getText());
        m.setCharacteristics(txtCharacteristics.getText());
        MarketChannelComboCatalog mccc = bz.getMarketChannelComboCatalog();

        for(int i=0;i<tblChannels.getRowCount();i++){
            if(tblChannels.getValueAt(i, 1).equals("Yes")){
                MarketChannelAssignment tvchannelteenmarket = mccc.newMarketChannelCombo(m, (Channel) tblChannels.getValueAt(i, 0));
                m.addValidChannel((Channel) tblChannels.getValueAt(i, 0));
            }
        }
    }//GEN-LAST:event_btnSaveActionPerformed


