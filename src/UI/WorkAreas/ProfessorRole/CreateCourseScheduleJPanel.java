    private void btnSaveActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnSaveActionPerformed
        // TODO add your handling code here:
        String term = String.valueOf(optionsTerm.getSelectedItem());
        String startDate = txtStartDate.getText().trim();
        String endDate = txtEndDate.getText().trim();
        String language = String.valueOf(optionsLanguage.getSelectedItem());
        String region = String.valueOf(optionsRegion.getSelectedItem());
        
        if(startDate.isEmpty() || endDate.isEmpty()){
            JOptionPane.showMessageDialog(this, "Please make sure all fields are filled in before proceeding.", "Validation Error", JOptionPane.ERROR_MESSAGE);
            return;
        }
        
        if(!(startDate.matches("^(0[1-9]|1[0-2])/(0[1-9]|[12][0-9]|3[01])/\\d{4}$") || startDate.matches("^(0[1-9]|1[0-2])/(0[1-9]|[12][0-9]|3[01])/\\d{4}$"))){
            JOptionPane.showMessageDialog(this, "Please enter a valid date format 'mm/dd/yyyy'.", "Validation Error", JOptionPane.ERROR_MESSAGE);
            return;
        }
        
        education.getCourseScheduleDirectory().addCourseSchedule("", course.getCourseId(), startDate, endDate, term.split("\\s+")[0], Integer.valueOf(term.split("\\s+")[1]), course.getProfessorOwnerId(), language, region, 0);
        JOptionPane.showMessageDialog(this,"Course schedule created successfuly!");
        optionsTerm.setSelectedIndex(0);
        txtStartDate.setText("");
        txtEndDate.setText("");
        optionsLanguage.setSelectedIndex(0);
        optionsRegion.setSelectedIndex(0);
        
        userProcessContainer.remove(this);
        CardLayout layout = (CardLayout) userProcessContainer.getLayout();
        layout.previous(userProcessContainer);
    }//GEN-LAST:event_btnSaveActionPerformed

