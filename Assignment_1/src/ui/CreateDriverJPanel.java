    private void btnSaveActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnSaveActionPerformed
        // TODO add your handling code here:
        // Validation if any field is left empty
        if(txtIssueDate.getText().isEmpty() || txtExpDate.getText().isEmpty() || txtLicesnseNo.getText().isEmpty()){
           JOptionPane.showMessageDialog(null, "Please enter all the fields");
            return; 
        }
        
        String licenseNo = txtLicesnseNo.getText();
        String issueDate = txtIssueDate.getText();
        String expDate = txtExpDate.getText();
        
        driver.setLicenseNumber(licenseNo);
        driver.setIssueDate(issueDate);
        driver.setExpirationDate(expDate);
        
        JOptionPane.showMessageDialog(this, "Driver Information Saved");
    }//GEN-LAST:event_btnSaveActionPerformed


