    private void btnSaveActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnSaveActionPerformed
        // TODO add your handling code here:
        
        // Validation if any field is left empty
        if(txtCorporateId.getText().isEmpty() || txtPremiumAmt.getText().isEmpty() || txtDeductableAmt.getText().isEmpty()
         || TxtAreaBenefits.getText().isEmpty() || !(isClaimedNoRadioBtn.isSelected() || isClaimedYesRadioBtn.isSelected())){
           JOptionPane.showMessageDialog(null, "Please enter all the fields");
            return; 
        }
        
        boolean checkIfNumber;
        String prAmt = (txtPremiumAmt.getText());
        checkIfNumber = prAmt.matches("^[0-9]+$");
        if(!checkIfNumber) {
            JOptionPane.showMessageDialog(null, "Premium Amount must have digits only");
            return;
        }        
        
        String dedAmt = txtDeductableAmt.getText();
        checkIfNumber = dedAmt.matches("^[0-9]+$");
        if(!checkIfNumber) {
            JOptionPane.showMessageDialog(null, "Deductable Amount must have digits only");
            return;
        }
        
        String corporateId = txtCorporateId.getText();
        long premiumAmt =Long.parseLong(txtPremiumAmt.getText()); 
        long deductableAmt =Long.parseLong(txtDeductableAmt.getText()); 
        String benefits= TxtAreaBenefits.getText();
        String claimed = "No";
        if(isClaimedYesRadioBtn.isSelected()){
            claimed = "Yes";
         }
        

        insuarance.setCorporateIdNumber(corporateId);
        insuarance.setPremiumAmount(premiumAmt);
        insuarance.setDeductableAmount(deductableAmt);
        insuarance.setBenefits(benefits);
        insuarance.setIsClaimed(claimed);
        
        JOptionPane.showMessageDialog(this, "Insuarance Information Saved");
        
    }//GEN-LAST:event_btnSaveActionPerformed

